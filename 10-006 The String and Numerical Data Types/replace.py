# Original sentence with exclamation marks
sentence = "The!quick!brown!fox!jumps!over!the!lazy!dog."

# Remove exclamation marks by replacing them with spaces
sentence_without_exclamation = sentence.replace("!", " ")

# Print the sentence without exclamation marks
print(sentence_without_exclamation)

# Convert the sentence to uppercase
upper_case_sentence = sentence_without_exclamation.upper()

# Print the sentence in uppercase
print(upper_case_sentence)

# Reverse the sentence
reversed_sentence = sentence_without_exclamation[::-1]

# Print the reversed sentence
print(reversed_sentence)
